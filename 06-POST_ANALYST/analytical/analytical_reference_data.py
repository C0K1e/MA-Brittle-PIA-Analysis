def get_reference_value(metric, case_type, crit, case_x, m):
    """
    Zentraler Zugriff auf vorberechnete analytische Referenzwerte.

    Args:
        metric:    'Veff', 'Aeff', etc.
        case_type: 'PWH', 'PWHR', 'BEAM1', etc.
        crit:      'PIA' oder 'S1'
        case_x:    Geometrie-Schluessel (int, z.B. 20, 40, 60, ...)
        m:         Weibull-Modul (1..50)

    Returns:
        float oder None (None = keine Daten vorhanden)
    """
    # 1. Metrik pruefen
    if metric not in REFERENCE_DATA:
        print(f"   [ref_data] WARNUNG: Unbekannte Metrik '{metric}'. "
              f"Verfuegbar: {list(REFERENCE_DATA.keys())}")
        return None

    metric_data = REFERENCE_DATA[metric]

    # 2. Case-Type pruefen
    if case_type not in metric_data:
        print(f"   [ref_data] WARNUNG: Kein {metric}-Eintrag fuer Case '{case_type}'. "
              f"Verfuegbar: {list(metric_data.keys())}")
        return None

    case_data = metric_data[case_type]

    # 3. Kriterium pruefen
    if crit not in case_data:
        print(f"   [ref_data] WARNUNG: Kein Kriterium '{crit}' fuer {metric}/{case_type}. "
              f"Verfuegbar: {list(case_data.keys())}")
        return None

    crit_data = case_data[crit]

    # 4. CASE_X pruefen
    if case_x not in crit_data:
        avail = sorted(crit_data.keys())
        print(f"   [ref_data] WARNUNG: Kein CASE_X={case_x} fuer {metric}/{case_type}/{crit}. "
              f"Verfuegbar: {avail}")
        return None

    table = crit_data[case_x]

    # 5. m-Bereich pruefen
    m_idx = int(round(float(m)))
    if m_idx < 1 or m_idx > len(table):
        print(f"   [ref_data] WARNUNG: m={m} ausserhalb Bereich 1..{len(table)} "
              f"fuer {metric}/{case_type}/{crit}/CASE_X={case_x}.")
        return None

    return table[m_idx - 1]


def get_reference_series(metric, case_type, crit, case_x):
    """
    Gibt die komplette Werte-Liste (m=1..50) zurueck.

    Args:
        metric:    'Veff', 'Aeff', etc.
        case_type: 'PWH', 'PWHR', etc.
        crit:      'PIA' oder 'S1'
        case_x:    Geometrie-Schluessel (int)

    Returns:
        list[float] oder None (None = keine Daten vorhanden)
    """
    try:
        table = REFERENCE_DATA[metric][case_type][crit][case_x]
        return list(table)  # Kopie zurueckgeben
    except (KeyError, TypeError):
        return None


def has_reference_data(metric, case_type, crit=None, case_x=None):
    """
    Prueft ob Referenzdaten fuer gegebene Kombination existieren.

    Args:
        metric:    'Veff', 'Aeff', etc.
        case_type: 'PWH', 'PWHR', etc.
        crit:      Optional - 'PIA' oder 'S1'
        case_x:    Optional - Geometrie-Schluessel (int)

    Returns:
        bool: True wenn (nicht-leere) Daten vorhanden
    """
    if metric not in REFERENCE_DATA:
        return False
    if case_type not in REFERENCE_DATA[metric]:
        return False

    case_data = REFERENCE_DATA[metric][case_type]
    if not case_data:
        return False

    if crit is not None:
        if crit not in case_data:
            return False
        crit_data = case_data[crit]
        if not crit_data:
            return False

        if case_x is not None:
            return case_x in crit_data and len(crit_data[case_x]) > 0

    return True


def available_case_x_keys(metric, case_type, crit):
    """
    Gibt verfuegbare CASE_X-Keys zurueck.

    Args:
        metric:    'Veff', 'Aeff', etc.
        case_type: 'PWH', 'PWHR', etc.
        crit:      'PIA' oder 'S1'

    Returns:
        list[int]: Sortierte Liste der verfuegbaren CASE_X-Keys, oder []
    """
    try:
        crit_data = REFERENCE_DATA[metric][case_type][crit]
        return sorted(crit_data.keys())
    except (KeyError, TypeError):
        return []
